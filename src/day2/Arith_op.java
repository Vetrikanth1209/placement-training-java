package day2;

public class Arith_op {
	public static void main (String[] args) {
		int a =20;
		int b =10;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		/////If you want to access last digit 
		int num1 = 1234;
		System.out.println(num1%10);
		
		/////If you want to access all the number without last digit
		int num2 = 1234;
		System.out.println(num2/10);
		
	}
}
